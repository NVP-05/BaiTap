package com.data.javarest06.controller;

public class BT01Controller {
//    Một HTTP Request bao gồm bốn thành phần chính: Method, URL, Headers và Body.
//    Method xác định hành động muốn thực hiện trên tài nguyên, ví dụ: GET để lấy
//    dữ liệu, POST để gửi dữ liệu mới, PUT để cập nhật, và DELETE để xóa tài nguyên.
//        URL (Uniform Resource Locator) chỉ định địa chỉ tài nguyên trên máy chủ mà
//    client muốn truy cập. Headers chứa thông tin bổ sung như loại dữ liệu (Content-Type)
//    , thông tin xác thực (Authorization), hoặc dữ liệu liên quan đến trình duyệt và client.
//    Cuối cùng, Body là phần nội dung đi kèm trong request, thường xuất hiện trong các method
//    như POST hoặc PUT, dùng để gửi dữ liệu như JSON, XML hoặc biểu mẫu. Tất cả các thành
//    phần này phối hợp với nhau giúp client giao tiếp hiệu quả với server trong môi trường web.
}
