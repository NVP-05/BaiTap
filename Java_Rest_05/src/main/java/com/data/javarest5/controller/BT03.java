public class BT03 {
//
//    RESTful API là một kiểu kiến trúc thiết kế dịch vụ web tuân theo các nguyên tắc của REST
//            (Representational State Transfer). Một trong những nguyên lý quan trọng là stateless,
//    nghĩa là mỗi yêu cầu từ client đến server phải chứa đầy đủ thông tin cần thiết, server không
//    lưu trạng thái phiên làm việc của client. RESTful API cũng tuân theo mô hình client-server, giúp
//    tách biệt giao diện người dùng (client) khỏi logic xử lý dữ liệu (server), từ đó nâng cao khả năng
//    mở rộng và bảo trì hệ thống. Ngoài ra, RESTful API sử dụng các HTTP methods như GET (lấy dữ liệu),
//    POST (tạo mới), PUT (cập nhật), DELETE (xóa) để thực hiện các thao tác CRUD trên tài nguyên. Mỗi tài
//    nguyên thường được đại diện bởi một URI cụ thể và phản hồi dữ liệu chủ yếu ở định dạng JSON hoặc XML.
}
