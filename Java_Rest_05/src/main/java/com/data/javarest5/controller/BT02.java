package com.data.javarest5.controller;

public class BT02 {
//    Bảng so sánh giữa SOAP và REST
//
//    Kiến trúc
//
//    SOAP: Có thể là stateful hoặc stateless (thường là stateful).
//
//    REST: Luôn stateless (không lưu trạng thái phía server).
//
//    Định dạng dữ liệu
//
//    SOAP: Chỉ hỗ trợ XML.
//
//    REST: Hỗ trợ nhiều định dạng như JSON, XML, HTML, YAML,…
//
//    Tính linh hoạt (các phương thức HTTP)
//
//    SOAP: Không chỉ sử dụng HTTP, có thể dùng các giao thức khác như SMTP.
//
//            REST: Chủ yếu sử dụng các phương thức HTTP như GET, POST, PUT, DELETE.
//
//    Tính bảo mật
//
//    SOAP: Hỗ trợ WS-Security – một tiêu chuẩn bảo mật mạnh mẽ cho các dịch vụ web.
//
//    REST: Bảo mật chủ yếu dựa vào HTTPS, đơn giản hơn so với SOAP.
}
