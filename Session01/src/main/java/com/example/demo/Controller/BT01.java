package com.example.demo.Controller;

public class BT01 {
    // So sánh Spring MVC vs Spring Boot
    /*
    * # Khái niệm
    * - Spring MVC là 1 module của Spring Framework hỗ trợ phát triển ứng dụng web theo mô hình MVC
    * - Spring Boot là 1 framework độc lập được xây dựng trên nền tảng Spring, giúp tạo ứng dụng nhanh chóng và giảm cấu hình phức tạp
    * # Cấu hình
    * - Spring MVC: cấu hình bằng XML hoặc JAVA
    * - Spring Boot: Cấu hình đơn giản hơn bằng cách sử dụng Auto Configuration chỉ cần cấu hình tối thiểu trong application.properties
    * # Khởi tạo dự án
    * - Spring MVC: phải cấu hình thủ công bằng Gradle/Maven
    * - Spring Boot: có thể sử dụng Spring Initializr để có thể khởi tạo dự án nhanh chóng
    * # Quản lý phụ thuộc
    * - Spring MVC: phải cấu hình thủ công bằng Gradle/Maven nên phải khai báo tất cả các dependency cần thiết.
    * - Spring Boot: Spring Boot cung cấp các starter dependency giúp đơn giản hóa quản lý.
    * # THời gian phát triển
    * - Spring MVC: Mất nhiều thời gian hơn do phải cấu hình nhiều
    * - Spring Boot: Tiết kiệm thời gian hơn nhờ auto-configuration
    */
}
